void main(List<String> args) {
  var age = 30;
  print(age ~/=
      2); // it performs division with 2 and store result in the "age" variable
  print(age); // contains the new value of age
  print(age = 30);
  print(age);
  print(age += 2);
  print(age *= 2);
  print(age -= 2);
  // bitwise or and AND operations can be used and assign the value to the age variable
}
