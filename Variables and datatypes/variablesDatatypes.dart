void main(List<String> args) {
  const name = 'khirman';
  final name2 = 'Rahma';
  var name3 = 'Mano';
  // assigning one variable to other one
  String v1 = 'Adnan';
  var v2 = 'khan';
  v1 = v2;
  print(v1);
  print(name + name2 + name3);
  print("hello world");
  // assigning constant value to the final variable
  const a = 20;
  final b = a;
  print(b);
  // constants cannot be assigned to a non constant value
}
